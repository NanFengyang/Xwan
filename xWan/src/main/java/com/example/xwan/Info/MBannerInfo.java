package com.example.xwan.Info;

public class MBannerInfo {
	// 排序id
	private String advertPosSort;
	// 图片url
	private int advertPicUrl;
	// 广告Id
	private String advertId;
	// 广告标题
	private String adverTitle;

	public String getAdverTitle() {
		return adverTitle;
	}

	public void setAdverTitle(String adverTitle) {
		this.adverTitle = adverTitle;
	}

	public String getAdvertPosSort() {
		return advertPosSort;
	}

	public void setAdvertPosSort(String advertPosSort) {
		this.advertPosSort = advertPosSort;
	}

	public int getAdvertPicUrl() {
		return advertPicUrl;
	}

	public void setAdvertPicUrl(int advertPicUrl) {
		this.advertPicUrl = advertPicUrl;
	}

	public String getAdvertId() {
		return advertId;
	}

	public void setAdvertId(String advertId) {
		this.advertId = advertId;
	}

}
